def test_aes_metrics():
    print("=== Pengujian Metrik S-box AES ===")
    print(f"S-box AES: {AES_SBOX[:16]}... (first 16 values)")
    
    # Hitung semua metrik
    nl_result = compute_nl(AES_SBOX)
    du_result = compute_du(AES_SBOX)
