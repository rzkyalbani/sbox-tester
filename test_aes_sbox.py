    ci_result = compute_ci(AES_SBOX)
    to_result = compute_to(AES_SBOX)
    
    print("\n=== Hasil Metrik untuk S-box AES ===")
    print(f"Non-Linearity (NL):         {nl_result}")
    print(f"Differential Uniformity (DU): {du_result}")
    print(f"Algebraic Degree (AD):       {ad_result}")
    print(f"Linear Approximation Prob (LAP): {lap_result:.6f}")
    print(f"Strict Avalanche Criterion (SAC): {sac_result:.6f}")
    print(f"BIC-Nonlinearity (BIC-NL):   {bic_nl_result:.6f}")
    print(f"BIC-SAC (BIC-SAC):           {bic_sac_result:.6f}")
    print(f"Differential Approximation Prob (DAP): {dap_result:.6f}")
    print(f"Correlation Immunity (CI):   {ci_result:.6f}")
    print(f"Transparency Order (TO):     {to_result:.6f}")
    
    print("\n=== Nilai Referensi untuk AES S-box ===")
    print("Non-Linearity (NL):         112")
    print("Differential Uniformity (DU): 4")
    print("Algebraic Degree (AD):       7")
    print("Linear Approximation Prob (LAP): 0.0625 (16/256)")
    
    print("\n=== Perbandingan dengan Referensi ===")
    print(f"NL cocok: {'YA' if nl_result == 112 else 'TIDAK'} (nilai: {nl_result})")
    print(f"DU cocok: {'YA' if du_result == 4 else 'TIDAK'} (nilai: {du_result})")
    print(f"AD cocok: {'YA' if ad_result == 7 else 'TIDAK'} (nilai: {ad_result})")
